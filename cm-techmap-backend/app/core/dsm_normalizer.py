"""
CM TECHMAP — DSM Normalizer & Offset Extraction

This module handles the critical transformation of raw photogrammetric DSM/DTM
outputs from NodeODM into web-ready elevation data. Based on the engineering
principles documented in the research transcript:

  1. NORMALIZATION: Raw DSM from NodeODM contains absolute elevations
     (e.g., 800m+ for areas in Minas Gerais). The frontend terrain renderer
     needs relative heights (0-N meters above ground). We compute the
     Normalized DSM (nDSM = DSM - DTM) when DTM is available, or subtract
     the minimum elevation as a fallback.

  2. OFFSET EXTRACTION: OBJ files from photogrammetry are NOT georeferenced.
     The offset.xyz file contains the Float64 barycentric translation vector
     that maps local-origin coordinates back to global geodetic position.
     This is CRITICAL for preventing Z-jittering in WebGL/Three.js/MapLibre
     via the RTE (Relative-To-Eye) rendering technique.

  3. STATISTICS: Elevation statistics (min, max, mean, percentiles) are
     essential for the frontend to configure terrain exaggeration, building
     height extraction, and color ramp calibration.

References:
  - maps-research-transcript.md, Cap. 5 (offset.xyz / Z-jittering)
  - maps-research-transcript.md, Cap. 4 (RTE / Float64 → Float32 bridge)
"""

import logging
import os
from dataclasses import dataclass, field, asdict
from pathlib import Path
from typing import Any

import numpy as np

logger = logging.getLogger(__name__)


# ══════════════════════════════════════════════════════════════════════════════
# Data Structures
# ══════════════════════════════════════════════════════════════════════════════

@dataclass
class OffsetXYZ:
    """
    Barycentric translation vector for georeferencing local-origin models.

    In the context of the research transcript (Cap. 5), this is the
    vec{T} = [T_x, T_y, T_z] that translates local-coordinate meshes
    back to global geodetic position:

        P_global = P_local + T

    The offset is stored in Float64 precision to prevent Z-jittering.
    The frontend JavaScript V8 engine uses Float64 natively, so this
    vector can be used directly for RTE calculations.
    """
    x: float = 0.0       # Easting or Longitude (Float64)
    y: float = 0.0       # Northing or Latitude (Float64)
    z: float = 0.0       # Elevation in meters (Float64)
    crs: str = "EPSG:4326"
    epsg: int = 4326

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)

    def to_file(self, path: str | Path) -> None:
        """Write offset to a standard offset.xyz text file."""
        with open(path, "w") as f:
            f.write(f"{self.x:.10f} {self.y:.10f} {self.z:.6f}\n")
            f.write(f"# CRS: {self.crs}\n")
            f.write(f"# EPSG: {self.epsg}\n")
            f.write(f"# Generated by CM TECHMAP DSM Normalizer\n")
        logger.info(f"[OFFSET] Written to {path}: ({self.x}, {self.y}, {self.z})")

    @classmethod
    def from_file(cls, path: str | Path) -> "OffsetXYZ":
        """Read offset from a standard offset.xyz text file."""
        with open(path, "r") as f:
            line = f.readline().strip()
            parts = line.split()
            x, y, z = float(parts[0]), float(parts[1]), float(parts[2])

        crs = "EPSG:4326"
        epsg = 4326
        # Try to read CRS from comment lines
        with open(path, "r") as f:
            for raw_line in f:
                if "EPSG:" in raw_line and raw_line.startswith("#"):
                    try:
                        epsg_str = raw_line.split("EPSG:")[1].strip()
                        epsg = int(epsg_str)
                        crs = f"EPSG:{epsg}"
                    except (ValueError, IndexError):
                        pass

        return cls(x=x, y=y, z=z, crs=crs, epsg=epsg)


@dataclass
class ElevationStatistics:
    """Comprehensive elevation statistics for a DSM/nDSM raster."""
    min_m: float = 0.0
    max_m: float = 0.0
    mean_m: float = 0.0
    std_m: float = 0.0
    range_m: float = 0.0
    p5: float = 0.0
    p25: float = 0.0
    p50: float = 0.0     # median
    p75: float = 0.0
    p95: float = 0.0
    valid_pixels: int = 0
    nodata_pixels: int = 0
    total_pixels: int = 0
    is_normalized: bool = False

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


@dataclass
class NormalizedDSMResult:
    """Result of DSM normalization with all extracted metadata."""
    normalized_path: str = ""
    offset: OffsetXYZ = field(default_factory=OffsetXYZ)
    statistics: ElevationStatistics = field(default_factory=ElevationStatistics)
    method: str = ""      # "ndsm" | "min_subtraction" | "raw"
    original_crs: str = ""
    original_epsg: int = 0

    def to_dict(self) -> dict[str, Any]:
        return {
            "normalized_path": self.normalized_path,
            "offset": self.offset.to_dict(),
            "statistics": self.statistics.to_dict(),
            "method": self.method,
            "original_crs": self.original_crs,
            "original_epsg": self.original_epsg,
        }


